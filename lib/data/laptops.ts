export function getLaptopById(id: string) {
  return LAPTOPS.find(l => l.id === id);
}

export function getLaptopsByMaker(makerId: string) {
  return LAPTOPS.filter(l => l.maker === makerId);
}

export function getLaptopsByTag(tag: string) {
  return LAPTOPS.filter(l => l.tags.includes(tag));
}

export function getLaptopsByBudget(max: number) {
  return LAPTOPS.filter(l => l.price <= max);
}
